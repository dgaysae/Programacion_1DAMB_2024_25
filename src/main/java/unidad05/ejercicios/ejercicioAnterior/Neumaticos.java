/**
 * Enumerado con el orden de las ruedas.
 * @author diego
 */
public enum Neumaticos {
    /**
     * Representa a la rueda delantera derecha.
     */
    DELANTERO_DCHO,

    /**
     * Representa a la rueda delantera izquierda.
     */
    DELANTERO_IZQDO,

    /**
     * Representa a la rueda trasera derecha.
     */
    TRASERO_DCHO,

    /**
     * Representa a la rueda trasera izquierda.
     */
    TRASERO_IZQDO;
}